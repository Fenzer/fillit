#include "libft.h"

void	ft_xorswap(void *p1, void *p2)
{
	if (p1 == p2)
		return ;
	*(int *)p1 ^= *(int *)p2;
	*(int *)p2 ^= *(int *)p1;
	*(int *)p1 ^= *(int *)p2;
}
