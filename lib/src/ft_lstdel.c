#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *buf;

	if (!alst || !*alst)
		return ;
	while (*alst)
	{
		del((*alst)->content, (*alst)->content_size);
		buf = (*alst)->next;
		free(*alst);
		*alst = buf;
	}
}
