#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char *rt;

	rt = NULL;
	while (*s)
	{
		if (*s == (char)c)
			rt = s;
		s++;
	}
	if (*s == (char)c)
		rt = s;
	return ((char *)rt);
}
