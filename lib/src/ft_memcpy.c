#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
		ft_memset(dest + i, (int)*((unsigned char *)src + i), 1);
	return (dest);
}
