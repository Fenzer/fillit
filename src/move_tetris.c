#include "fillit.h"

void	move_tetris(int y, int x, t_tetri *tetris)
{
	int i;

	i = 0;
	while (i < 4)
	{
		tetris->coord[i][0] += y;
		tetris->coord[i][1] += x;
		i++;
	}
}
